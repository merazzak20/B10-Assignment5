
// Get Input Value
function getInputValue(id){
    return parseFloat(document.getElementById(id).value);
}

// Get Inner Text
function getInnerText(id){
    return parseFloat(document.getElementById(id).innerText);
}

// Get Tag
function tag(id){
    return document.getElementById(id);
}


